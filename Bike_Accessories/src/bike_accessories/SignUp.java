    private void signupbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signupbtnActionPerformed
        String name, user, phone, email, pass;
        name = nametf.getText();
        user = usernametf.getText();
        phone = phonenumbertf.getText();
        email = emailaddresstf.getText();
        pass = passwordtf.getText();
        try {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/bike_accessories?zeroDateTimeBehavior=convertToNull", "root", "");
            Statement st = con.createStatement();
            st.executeUpdate("insert into register values('" + name + "','" + user + "','" + phone + "','" + email + "','" + pass + "')");
        } catch (SQLException e) {
            e.printStackTrace();
        }
        if (nametf.getText().isEmpty() || usernametf.getText().isEmpty() || phonenumbertf.getText().isEmpty() || emailaddresstf.getText().isEmpty() || passwordtf.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Account Creation Failed");
        } else {
            Main_Frame main = new Main_Frame();
            main.usernamelbl.setText(usernametf.getText());
            JOptionPane.showMessageDialog(null, "Account Created Successfully");
            dispose();
            main.setVisible(true);
        }
    }//GEN-LAST:event_signupbtnActionPerformed

