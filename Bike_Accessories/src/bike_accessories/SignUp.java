    private void phonenumbertfKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_phonenumbertfKeyPressed
        String mobile = phonenumbertf.getText();
        int length = mobile.length();
        char c = evt.getKeyChar();
        if (evt.getKeyChar() >= '0' && evt.getKeyChar() <= '9') {
            if (length < 10) {
                phonenumbertf.setEditable(true);
            } else {
                phonenumbertf.setEditable(false);
            }
        } else if (evt.getExtendedKeyCode() == KeyEvent.VK_BACK_SPACE || evt.getExtendedKeyCode() == KeyEvent.VK_DELETE) {
            phonenumbertf.setEditable(true);
        } else {
            phonenumbertf.setEditable(false);
        }
    }//GEN-LAST:event_phonenumbertfKeyPressed

